    public function handle(): int
    {
        $this->info('🚀 Starting TranslaleModel batch update from $translations variable...');
        $this->newLine();

        $start_time = microtime(true);

        $totalRecords = count($this->translations);
        $this->info("📊 Total records to process: {$totalRecords}");
        $this->newLine();

        $this->info('💾 Updating TranslaleModel...');

        $updated = 0;
        $created = 0;
        $failed = 0;

        $progressBar = $this->output->createProgressBar($totalRecords);
        $progressBar->start();

        foreach ($this->translations as $translation) {
            try {
                $model = \App\Models\TranslaleModel::updateOrCreate(
                    ['name' => $translation['name']],
                    [
                        'value' => $translation['value'] ?? '',
                        'valueFr' => $translation['valueFr'] ?? '',
                        'valueEn' => $translation['valueEn'] ?? '',
                        'valueEs' => $translation['valueEs'] ?? '',
                        'valueTr' => $translation['valueTr'] ?? '',
                        'valueRu' => $translation['valueRu'] ?? '',
                        'valueDe' => $translation['valueDe'] ?? '',
                    ]
                );

                if ($model->wasRecentlyCreated) {
                    $created++;
                } else {
                    $updated++;
                }
            } catch (\Exception $e) {
                $failed++;
                \Illuminate\Support\Facades\Log::error("Failed to update translation: {$translation['name']} - " . $e->getMessage());
            }

            $progressBar->advance();
        }

        $progressBar->finish();
        $this->newLine(2);

        $end_time = microtime(true);
        $execution_time = formatSolde(($end_time - $start_time), 3);

        $this->info('✅ Batch update completed!');
        $this->table(
            ['Metric', 'Count'],
            [
                ['Created', $created],
                ['Updated', $updated],
                ['Failed', $failed],
                ['Total', $totalRecords],
            ]
        );
        $this->info("⏱️  Execution time: {$execution_time} seconds");

        \Illuminate\Support\Facades\Log::info(self::class . " - Execution time: {$execution_time}");

        return $failed > 0 ? 1 : 0;
    }
